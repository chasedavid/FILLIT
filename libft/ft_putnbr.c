#include "libft.h"

void	ft_putnbr(int num)
{
	if (num == -2147483648)
	{
		ft_putstr("-2147483648");
		return ;
	}
	if (num > 2147483647 || num < -2147483648)
		num = 0;
	if (num < 0)
	{
		ft_putchar('-');
		num = num * -1;
	}
	if (num >= 10)
	{
		ft_putnbr(num / 10);
		num = num % 10;
		ft_putchar(num + 48);
	}
	else
		ft_putchar(num + 48);
}
