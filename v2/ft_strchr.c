#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	size_t i;

	i = 0;
	while (s[i] != c)
	{
		if ((i == ft_strlen(s)) || (s[i] == '\0'))
			return (NULL);
		i++;
	}
	return ((char*)&s[i]);
}
